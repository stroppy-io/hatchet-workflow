package dbconfig

import (
	"fmt"
	"strings"

	"github.com/stroppy-io/stroppy-cloud/internal/domain/types"
)

// PicodataAdvertiseHostPlaceholder is left in the rendered picodata.yaml in
// place of the per-node advertise hostname. The agent substitutes it at run
// time so the same body can be edited once and reused on every instance.
const PicodataAdvertiseHostPlaceholder = "__PICODATA_ADVERTISE_HOST__"

// RenderPicodataConfOpts collects the inputs RenderPicodataConf needs.
type RenderPicodataConfOpts struct {
	Replication   int               // replication factor for the default tier
	Options       map[string]string // user overrides (e.g. memtx_memory: "4GB")
	TotalMemoryMB int               // memory budget for "25%"-style defaults
}

// RenderPicodataConf returns the picodata.yaml body the agent writes into
// /etc/picodata/picodata.yaml. AdvertiseHost is left as a placeholder.
func RenderPicodataConf(opts RenderPicodataConfOpts) string {
	defaults := types.PicodataDefaults("25.3")
	ResolveMemoryPercents(defaults, opts.TotalMemoryMB)
	for k, v := range opts.Options {
		defaults[k] = v
	}

	replication := opts.Replication
	if replication < 1 {
		replication = 2
	}

	memtxBytes := picodataMemtxBytes(defaults["memtx_memory"])

	var b strings.Builder
	b.WriteString("# Generated by stroppy-agent\n")
	b.WriteString("cluster:\n")
	b.WriteString("  name: stroppy-cluster\n")
	b.WriteString("  tier:\n")
	b.WriteString("    default:\n")
	fmt.Fprintf(&b, "      replication_factor: %d\n", replication)
	b.WriteString("      can_vote: true\n")
	b.WriteString("\n")

	b.WriteString("instance:\n")
	b.WriteString("  tier: default\n")
	b.WriteString("  iproto:\n")
	b.WriteString("    listen: 0.0.0.0:3301\n")
	fmt.Fprintf(&b, "    advertise: %s:3301\n", PicodataAdvertiseHostPlaceholder)
	b.WriteString("  pg:\n")
	b.WriteString("    listen: 0.0.0.0:5432\n")
	fmt.Fprintf(&b, "    advertise: %s:5432\n", PicodataAdvertiseHostPlaceholder)
	b.WriteString("    ssl: false\n")
	b.WriteString("  http:\n")
	b.WriteString("    listen: 0.0.0.0:8081\n")
	b.WriteString("  memtx:\n")
	fmt.Fprintf(&b, "    memory: %d\n", memtxBytes)
	return b.String()
}

// SubstitutePicodataPlaceholders fills the per-node advertise host into a
// rendered or override picodata.yaml just before the agent writes it.
func SubstitutePicodataPlaceholders(body, advertiseHost string) string {
	return strings.ReplaceAll(body, PicodataAdvertiseHostPlaceholder, advertiseHost)
}

// picodataMemtxBytes converts an "<N>MB" string from defaults into bytes.
// Falls back to picodata's stroppy default of 2 GiB when missing or unparsable.
func picodataMemtxBytes(s string) int {
	v := strings.TrimSuffix(s, "MB")
	var mb int
	if _, err := fmt.Sscanf(v, "%d", &mb); err == nil && mb > 0 {
		return mb * 1024 * 1024
	}
	return 2 * 1024 * 1024 * 1024
}
