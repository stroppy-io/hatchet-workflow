package dbconfig

import (
	"fmt"
	"sort"
	"strings"
)

// Aux placeholders. Per-node-only fields stay as placeholders so a single
// edited body can be reused on every node with correct unique values.
const (
	PatroniNamePlaceholder        = "__PATRONI_NAME__"
	PatroniNodeNamePlaceholder    = "__PATRONI_NODE_NAME__"
	PatroniConnectAddrPlaceholder = "__PATRONI_CONNECT_ADDR__"
	PatroniEtcdHostsPlaceholder   = "__PATRONI_ETCD_HOSTS__"
	PgBouncerPGHostPlaceholder    = "__PGBOUNCER_PG_HOST__"
)

// RenderPatroniConfOpts collects what RenderPatroniConf needs.
type RenderPatroniConfOpts struct {
	PGVersion string            // PG major, e.g. "16"
	SyncMode  bool              // synchronous_mode
	SyncCount int               // synchronous_node_count
	PGOptions map[string]string // postgresql parameters block
}

// RenderPatroniConf returns the patroni.yml body. Per-node fields (scope,
// name, connect_address, etcd hosts) are placeholders the agent substitutes
// per node before writing.
func RenderPatroniConf(opts RenderPatroniConfOpts) string {
	pgVersion := opts.PGVersion
	if pgVersion == "" {
		pgVersion = "16"
	}
	syncMode := "false"
	if opts.SyncMode {
		syncMode = "true"
	}

	var b strings.Builder
	b.WriteString("# Generated by stroppy-agent\n")
	fmt.Fprintf(&b, "scope: %s\n", PatroniNamePlaceholder)
	fmt.Fprintf(&b, "name: %s\n\n", PatroniNodeNamePlaceholder)

	b.WriteString("restapi:\n")
	b.WriteString("  listen: 0.0.0.0:8008\n")
	fmt.Fprintf(&b, "  connect_address: %s:8008\n\n", PatroniConnectAddrPlaceholder)

	b.WriteString("etcd3:\n")
	fmt.Fprintf(&b, "  hosts: %s\n\n", PatroniEtcdHostsPlaceholder)

	b.WriteString("bootstrap:\n")
	b.WriteString("  dcs:\n")
	b.WriteString("    ttl: 30\n")
	b.WriteString("    loop_wait: 10\n")
	b.WriteString("    retry_timeout: 10\n")
	b.WriteString("    maximum_lag_on_failover: 1048576\n")
	fmt.Fprintf(&b, "    synchronous_mode: %s\n", syncMode)
	if opts.SyncMode && opts.SyncCount > 0 {
		fmt.Fprintf(&b, "    synchronous_node_count: %d\n", opts.SyncCount)
	}
	b.WriteString("    postgresql:\n")
	b.WriteString("      use_pg_rewind: true\n")
	b.WriteString("      parameters:\n")
	b.WriteString("        wal_level: replica\n")
	b.WriteString("        hot_standby: 'on'\n")
	b.WriteString("        max_wal_senders: 10\n")
	b.WriteString("        max_replication_slots: 10\n")

	keys := make([]string, 0, len(opts.PGOptions))
	for k := range opts.PGOptions {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	for _, k := range keys {
		fmt.Fprintf(&b, "        %s: '%s'\n", k, opts.PGOptions[k])
	}

	b.WriteString("      pg_hba:\n")
	b.WriteString("        - local all all trust\n")
	b.WriteString("        - host all all 0.0.0.0/0 trust\n")
	b.WriteString("        - host replication all 0.0.0.0/0 trust\n")
	b.WriteString("  initdb:\n")
	b.WriteString("    - encoding: UTF8\n")
	b.WriteString("    - data-checksums\n\n")

	dataDir := fmt.Sprintf("/var/lib/postgresql/%s/main", pgVersion)
	b.WriteString("postgresql:\n")
	b.WriteString("  listen: 0.0.0.0:5432\n")
	fmt.Fprintf(&b, "  connect_address: %s:5432\n", PatroniConnectAddrPlaceholder)
	fmt.Fprintf(&b, "  data_dir: %s\n", dataDir)
	fmt.Fprintf(&b, "  bin_dir: /usr/lib/postgresql/%s/bin\n", pgVersion)
	b.WriteString("  pgpass: /tmp/pgpass\n")
	b.WriteString("  authentication:\n")
	b.WriteString("    replication:\n")
	b.WriteString("      username: postgres\n")
	b.WriteString("    superuser:\n")
	b.WriteString("      username: postgres\n")
	return b.String()
}

// SubstitutePatroniPlaceholders fills the per-node Patroni values into a
// rendered or override body just before the agent writes it.
func SubstitutePatroniPlaceholders(body, scope, nodeName, connectAddr, etcdHosts string) string {
	body = strings.ReplaceAll(body, PatroniNamePlaceholder, scope)
	body = strings.ReplaceAll(body, PatroniNodeNamePlaceholder, nodeName)
	body = strings.ReplaceAll(body, PatroniConnectAddrPlaceholder, connectAddr)
	body = strings.ReplaceAll(body, PatroniEtcdHostsPlaceholder, etcdHosts)
	return body
}

// RenderPgBouncerConfOpts collects what RenderPgBouncerConf needs.
type RenderPgBouncerConfOpts struct {
	ListenPort      int
	PoolMode        string
	MaxClientConn   int
	DefaultPoolSize int
	PGPort          int
	AuthType        string
}

// RenderPgBouncerConf returns the pgbouncer.ini body. PGHost is left as a
// placeholder so the agent can substitute the per-node target (typically
// "localhost" because pgbouncer is colocated with PG, but kept generic for
// off-box deployments later).
func RenderPgBouncerConf(opts RenderPgBouncerConfOpts) string {
	if opts.ListenPort == 0 {
		opts.ListenPort = 6432
	}
	if opts.PoolMode == "" {
		opts.PoolMode = "transaction"
	}
	if opts.MaxClientConn == 0 {
		opts.MaxClientConn = 1000
	}
	if opts.DefaultPoolSize == 0 {
		opts.DefaultPoolSize = 25
	}
	if opts.PGPort == 0 {
		opts.PGPort = 5432
	}
	if opts.AuthType == "" {
		opts.AuthType = "trust"
	}
	return fmt.Sprintf(`; Generated by stroppy-agent
[databases]
* = host=%s port=%d

[pgbouncer]
listen_addr = 0.0.0.0
listen_port = %d
auth_type = %s
auth_file = /etc/pgbouncer/userlist.txt
pool_mode = %s
max_client_conn = %d
default_pool_size = %d
admin_users = postgres
pidfile = /var/run/pgbouncer/pgbouncer.pid
logfile = /var/log/pgbouncer/pgbouncer.log
`, PgBouncerPGHostPlaceholder, opts.PGPort, opts.ListenPort, opts.AuthType,
		opts.PoolMode, opts.MaxClientConn, opts.DefaultPoolSize)
}

// SubstitutePgBouncerPlaceholders fills the per-node PG host into a rendered
// or override body just before the agent writes it.
func SubstitutePgBouncerPlaceholders(body, pgHost string) string {
	if pgHost == "" {
		pgHost = "localhost"
	}
	return strings.ReplaceAll(body, PgBouncerPGHostPlaceholder, pgHost)
}

// RenderHAProxyConfOpts collects what RenderHAProxyConf needs.
type RenderHAProxyConfOpts struct {
	WritePort   int
	ReadPort    int
	Backends    []string // host:port list — full backend addresses, no placeholders
	HealthCheck string   // "patroni" | "mysql" | "tcp"
	PatroniPort int
}

// RenderHAProxyConf returns the haproxy.cfg body. Backends are global to the
// cluster, so this body is identical on every HAProxy node and needs no
// placeholders.
func RenderHAProxyConf(opts RenderHAProxyConfOpts) string {
	if opts.PatroniPort == 0 {
		opts.PatroniPort = 8008
	}

	var b strings.Builder
	b.WriteString("# Generated by stroppy-agent\n")
	b.WriteString("global\n")
	b.WriteString("    maxconn 4096\n")
	b.WriteString("    log stdout format raw local0\n\n")
	b.WriteString("defaults\n")
	b.WriteString("    mode tcp\n")
	b.WriteString("    timeout connect 5s\n")
	b.WriteString("    timeout client 30s\n")
	b.WriteString("    timeout server 30s\n")
	b.WriteString("    retries 3\n\n")

	fmt.Fprintf(&b, "frontend ft_write\n")
	fmt.Fprintf(&b, "    bind *:%d\n", opts.WritePort)
	b.WriteString("    default_backend bk_write\n\n")

	fmt.Fprintf(&b, "frontend ft_read\n")
	fmt.Fprintf(&b, "    bind *:%d\n", opts.ReadPort)
	b.WriteString("    default_backend bk_read\n\n")

	switch opts.HealthCheck {
	case "patroni":
		b.WriteString("backend bk_write\n")
		fmt.Fprintf(&b, "    option httpchk GET /primary\n")
		fmt.Fprintf(&b, "    http-check expect status 200\n")
		b.WriteString("    default-server inter 3s fall 3 rise 2 on-marked-down shutdown-sessions\n")
		for i, ba := range opts.Backends {
			fmt.Fprintf(&b, "    server pg%d %s check port %d\n", i, ba, opts.PatroniPort)
		}
		b.WriteString("\n")
		b.WriteString("backend bk_read\n")
		fmt.Fprintf(&b, "    option httpchk GET /replica\n")
		fmt.Fprintf(&b, "    http-check expect status 200\n")
		b.WriteString("    balance roundrobin\n")
		b.WriteString("    default-server inter 3s fall 3 rise 2 on-marked-down shutdown-sessions\n")
		for i, ba := range opts.Backends {
			fmt.Fprintf(&b, "    server pg%d %s check port %d\n", i, ba, opts.PatroniPort)
		}
	case "mysql":
		b.WriteString("backend bk_write\n")
		b.WriteString("    option mysql-check user haproxy\n")
		b.WriteString("    default-server inter 3s fall 3 rise 2\n")
		for i, ba := range opts.Backends {
			fmt.Fprintf(&b, "    server mysql%d %s check\n", i, ba)
		}
		b.WriteString("\n")
		b.WriteString("backend bk_read\n")
		b.WriteString("    option mysql-check user haproxy\n")
		b.WriteString("    balance roundrobin\n")
		b.WriteString("    default-server inter 3s fall 3 rise 2\n")
		for i, ba := range opts.Backends {
			fmt.Fprintf(&b, "    server mysql%d %s check\n", i, ba)
		}
	default: // "tcp" — first backend = master (write), rest = replicas (read)
		b.WriteString("backend bk_write\n")
		b.WriteString("    option tcp-check\n")
		b.WriteString("    default-server inter 3s fall 3 rise 2\n")
		if len(opts.Backends) > 0 {
			fmt.Fprintf(&b, "    server srv0 %s check\n", opts.Backends[0])
		}
		b.WriteString("\n")
		b.WriteString("backend bk_read\n")
		b.WriteString("    option tcp-check\n")
		b.WriteString("    balance roundrobin\n")
		b.WriteString("    default-server inter 3s fall 3 rise 2\n")
		if len(opts.Backends) > 1 {
			for i, ba := range opts.Backends[1:] {
				fmt.Fprintf(&b, "    server srv%d %s check\n", i+1, ba)
			}
		} else if len(opts.Backends) > 0 {
			fmt.Fprintf(&b, "    server srv0 %s check\n", opts.Backends[0])
		}
	}
	return b.String()
}
