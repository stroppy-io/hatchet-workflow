package dbconfig

import (
	"fmt"
	"strings"
)

// YDB placeholder format. The renderer emits __YDB_HOST_<i>__ in the hosts
// section; the agent substitutes them with real hostnames at run time so the
// same edited body works on every node.
const ydbHostPlaceholderPrefix = "__YDB_HOST_"

func ydbHostPlaceholder(i int) string {
	return fmt.Sprintf("%s%d__", ydbHostPlaceholderPrefix, i)
}

// RenderYDBConfOpts collects the inputs RenderYDBStorageConf needs.
type RenderYDBConfOpts struct {
	HostCount      int    // number of static (storage) nodes — drives placeholder list
	DiskPath       string // "/ydb_data" in Docker, real disk on VM
	CPUs           int    // vCPUs hint for actor_system_config
	MemoryMB       int    // total RAM for memory_controller_config (85% mark)
	FaultTolerance string // "none" | "block-4-2" | "mirror-3-dc"
}

// RenderYDBDatabaseConfOpts collects the inputs RenderYDBDatabaseConf needs.
// Database (dynamic) nodes carry the cluster topology too — they need to know
// the storage hosts to register against, plus their own CPU/memory budget.
type RenderYDBDatabaseConfOpts struct {
	HostCount      int    // storage host count — same placeholder shape as the storage config
	DiskPath       string // mostly cosmetic on database nodes; kept for parity with the storage yaml so users see one consistent template
	CPUs           int    // vCPUs hint for actor_system_config (COMPUTE)
	MemoryMB       int    // total RAM for memory_controller_config (85% mark)
	FaultTolerance string // mirrored from cluster setting
}

// RenderYDBStorageConf returns the YDB static-node config.yaml body with
// node_type: STORAGE. Host addresses are emitted as __YDB_HOST_<i>__
// placeholders; the agent calls SubstituteYDBHostPlaceholders with the actual
// host list before writing.
func RenderYDBStorageConf(opts RenderYDBConfOpts) string {
	return renderYDBYAML(yDBYAMLOpts{
		nodeType:       "STORAGE",
		hostCount:      opts.HostCount,
		diskPath:       opts.DiskPath,
		cpus:           opts.CPUs,
		memoryMB:       opts.MemoryMB,
		faultTolerance: opts.FaultTolerance,
	})
}

// RenderYDBDatabaseConf returns the YDB dynamic-node config.yaml body with
// node_type: COMPUTE. Same cluster topology and placeholders as the storage
// config — it has to know the static hosts to talk to — but the actor system
// hint and memory budget reflect the database node's resources, not the
// storage node's.
func RenderYDBDatabaseConf(opts RenderYDBDatabaseConfOpts) string {
	return renderYDBYAML(yDBYAMLOpts{
		nodeType:       "COMPUTE",
		hostCount:      opts.HostCount,
		diskPath:       opts.DiskPath,
		cpus:           opts.CPUs,
		memoryMB:       opts.MemoryMB,
		faultTolerance: opts.FaultTolerance,
	})
}

type yDBYAMLOpts struct {
	nodeType       string // "STORAGE" or "COMPUTE"
	hostCount      int
	diskPath       string
	cpus           int
	memoryMB       int
	faultTolerance string
}

func renderYDBYAML(opts yDBYAMLOpts) string {
	diskPath := opts.diskPath
	if diskPath == "" {
		diskPath = "/ydb_data"
	}

	hostCount := opts.hostCount
	if hostCount < 1 {
		hostCount = 1
	}

	erasure := "none"
	if opts.faultTolerance != "" {
		erasure = opts.faultTolerance
	}

	zones := []string{"zone-a", "zone-b", "zone-c"}

	memMB := opts.memoryMB
	hardMB := 0
	if memMB > 0 {
		hardMB = memMB * 85 / 100
	}

	nodeType := opts.nodeType
	if nodeType == "" {
		nodeType = "STORAGE"
	}

	var b strings.Builder
	b.WriteString("# Generated by stroppy-agent\n")
	fmt.Fprintf(&b, "static_erasure: %s\n", erasure)

	// host_configs
	b.WriteString("host_configs:\n")
	b.WriteString("- drive:\n")
	fmt.Fprintf(&b, "  - path: %s/pdisk.data\n", diskPath)
	b.WriteString("    type: SSD\n")
	b.WriteString("  host_config_id: 1\n")

	// hosts
	b.WriteString("hosts:\n")
	for i := 0; i < hostCount; i++ {
		fmt.Fprintf(&b, "- host: %s\n", ydbHostPlaceholder(i))
		b.WriteString("  host_config_id: 1\n")
		b.WriteString("  walle_location:\n")
		fmt.Fprintf(&b, "    body: %d\n", i+1)
		fmt.Fprintf(&b, "    data_center: '%s'\n", zones[i%len(zones)])
		fmt.Fprintf(&b, "    rack: '%d'\n", i+1)
	}

	// domains_config
	b.WriteString("domains_config:\n")
	b.WriteString("  domain:\n")
	b.WriteString("  - name: Root\n")
	b.WriteString("    storage_pool_types:\n")
	b.WriteString("    - kind: ssd\n")
	b.WriteString("      pool_config:\n")
	b.WriteString("        box_id: 1\n")
	fmt.Fprintf(&b, "        erasure_species: %s\n", erasure)
	b.WriteString("        kind: ssd\n")
	if erasure == "mirror-3-dc" {
		b.WriteString("        geometry:\n")
		b.WriteString("          realm_level_begin: 10\n")
		b.WriteString("          realm_level_end: 20\n")
		b.WriteString("          domain_level_begin: 10\n")
		b.WriteString("          domain_level_end: 256\n")
	}
	b.WriteString("        pdisk_filter:\n")
	b.WriteString("        - property:\n")
	b.WriteString("          - type: SSD\n")
	b.WriteString("        vdisk_kind: Default\n")
	b.WriteString("  state_storage:\n")
	b.WriteString("  - ring:\n")
	b.WriteString("      node: [")
	for i := 0; i < hostCount; i++ {
		if i > 0 {
			b.WriteString(", ")
		}
		fmt.Fprintf(&b, "%d", i+1)
	}
	b.WriteString("]\n")
	fmt.Fprintf(&b, "      nto_select: %d\n", hostCount)
	b.WriteString("    ssid: 1\n")

	// table_service_config
	b.WriteString("table_service_config:\n  sql_version: 1\n")

	// actor_system_config — auto-tune threads from CPU count, hint node type.
	b.WriteString("actor_system_config:\n")
	b.WriteString("  use_auto_config: true\n")
	fmt.Fprintf(&b, "  node_type: %s\n", nodeType)
	if opts.cpus > 0 {
		fmt.Fprintf(&b, "  cpu_count: %d\n", opts.cpus)
	}

	// memory_controller_config — 85% of total RAM as the hard limit.
	if hardMB > 0 {
		b.WriteString("memory_controller_config:\n")
		fmt.Fprintf(&b, "  hard_limit_bytes: %d\n", int64(hardMB)*1024*1024)
	}

	// blob_storage_config
	b.WriteString("blob_storage_config:\n")
	b.WriteString("  service_set:\n")
	b.WriteString("    groups:\n")
	fmt.Fprintf(&b, "    - erasure_species: %s\n", erasure)
	b.WriteString("      rings:\n")
	if erasure == "mirror-3-dc" {
		for i := 0; i < hostCount; i++ {
			b.WriteString("      - fail_domains:\n")
			b.WriteString("        - vdisk_locations:\n")
			fmt.Fprintf(&b, "          - node_id: %d\n", i+1)
			b.WriteString("            pdisk_category: SSD\n")
			fmt.Fprintf(&b, "            path: %s/pdisk.data\n", diskPath)
		}
	} else {
		b.WriteString("      - fail_domains:\n")
		for i := 0; i < hostCount; i++ {
			b.WriteString("        - vdisk_locations:\n")
			fmt.Fprintf(&b, "          - node_id: %d\n", i+1)
			b.WriteString("            pdisk_category: SSD\n")
			fmt.Fprintf(&b, "            path: %s/pdisk.data\n", diskPath)
		}
	}

	// channel_profile_config — three identical channels.
	b.WriteString("channel_profile_config:\n")
	b.WriteString("  profile:\n")
	b.WriteString("  - channel:\n")
	for i := 0; i < 3; i++ {
		fmt.Fprintf(&b, "    - erasure_species: %s\n", erasure)
		b.WriteString("      pdisk_category: 0\n")
		b.WriteString("      storage_pool_kind: ssd\n")
	}

	// grpc_config
	b.WriteString("grpc_config:\n  port: 2136\n")

	return b.String()
}

// SubstituteYDBHostPlaceholders fills in real hostnames for __YDB_HOST_<i>__
// placeholders in either a rendered or override body.
func SubstituteYDBHostPlaceholders(body string, hosts []string) string {
	for i, h := range hosts {
		body = strings.ReplaceAll(body, ydbHostPlaceholder(i), h)
	}
	return body
}
