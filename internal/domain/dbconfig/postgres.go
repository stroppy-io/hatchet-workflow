// Package dbconfig renders database configuration files (postgresql.conf,
// my.cnf, ydb.yaml, etc.) from topology + per-component options.
//
// The same renderers are used by the dry-run preview (so the SPA can show
// the effective file contents on the Review step) and by the on-host agent
// when configuring a database. Keeping the logic in one place makes the
// preview byte-for-byte equal to what eventually lands on the box.
package dbconfig

import (
	"fmt"
	"sort"
	"strings"

	"github.com/stroppy-io/stroppy-cloud/internal/domain/types"
)

// RenderPostgresConfOpts collects everything RenderPostgresConf needs.
// Anything truly runtime-only (e.g. the host's actual /proc/meminfo) becomes
// a parameter so the function stays pure and testable.
type RenderPostgresConfOpts struct {
	Version       string            // PG major version, e.g. "16"
	Role          string            // "master" | "replica"
	Options       map[string]string // user overrides merged on top of defaults
	Patroni       bool              // when true, Patroni manages replication settings
	TotalMemoryMB int               // memory budget used to resolve "25%"-style defaults
}

// RenderPostgresConf returns the postgresql.conf body that the agent writes
// into /etc/postgresql/<version>/main/postgresql.conf, including the leading
// banner. Deterministic key ordering keeps output diff-friendly across runs.
func RenderPostgresConf(opts RenderPostgresConfOpts) string {
	version := opts.Version
	if version == "" {
		version = "16"
	}

	defaults := types.PostgresDefaults(version)

	// Mandatory streaming-replication knobs — only when Patroni isn't managing
	// replication itself.
	if !opts.Patroni {
		defaults["wal_level"] = "replica"
		defaults["max_wal_senders"] = "10"
		defaults["max_replication_slots"] = "10"
		if opts.Role == "replica" {
			defaults["hot_standby"] = "on"
		}
	}

	// User options override defaults.
	for k, v := range opts.Options {
		defaults[k] = v
	}

	// Resolve "25%"-style placeholders against the effective memory budget.
	ResolveMemoryPercents(defaults, opts.TotalMemoryMB)

	keys := make([]string, 0, len(defaults))
	for k := range defaults {
		keys = append(keys, k)
	}
	sort.Strings(keys)

	var b strings.Builder
	b.WriteString("# Generated by stroppy-agent\n")
	for _, k := range keys {
		fmt.Fprintf(&b, "%s = %s\n", k, defaults[k])
	}
	return b.String()
}

// PostgresPgHbaConf returns the pg_hba.conf content the agent writes alongside
// postgresql.conf. Static today; exposed here so the dry-run preview can show
// it in its own textarea.
func PostgresPgHbaConf() string {
	return `# Generated by stroppy-agent
local   all             all                 trust
host    all             all   127.0.0.1/32  trust
host    all             all   ::1/128       trust
host    all             all   0.0.0.0/0     trust
host    replication     all   0.0.0.0/0     trust
local   replication     all                 trust
`
}

// ResolveMemoryPercents replaces "25%"-style values in a postgresql.conf-like
// map with concrete "<N>MB" values, sized against totalMemoryMB. Floors at
// 32 MB and caps at 2048 MB to match the agent's historical behaviour for
// container-sized hosts.
func ResolveMemoryPercents(m map[string]string, totalMemoryMB int) {
	for k, v := range m {
		if !strings.HasSuffix(v, "%") {
			continue
		}
		var pct int
		if _, err := fmt.Sscanf(strings.TrimSuffix(v, "%"), "%d", &pct); err != nil || pct <= 0 {
			continue
		}
		valMB := totalMemoryMB * pct / 100
		if valMB < 32 {
			valMB = 32
		}
		if valMB > 2048 {
			valMB = 2048
		}
		m[k] = fmt.Sprintf("%dMB", valMB)
	}
}
