package dbconfig

import (
	"fmt"
	"sort"
	"strings"

	"github.com/stroppy-io/stroppy-cloud/internal/domain/types"
)

// MySQL placeholders the agent substitutes at run time. The renderer emits
// these instead of values that depend on the target machine, so the dry-run
// preview is identical across nodes and the same override text can be edited
// once and applied per-node with correct per-node values.
const (
	MySQLServerIDPlaceholder = "__MYSQL_SERVER_ID__"
	MySQLLocalHostPlaceholder = "__MYSQL_LOCAL_HOST__"
)

// RenderMySQLConfOpts collects everything RenderMySQLConf needs.
type RenderMySQLConfOpts struct {
	Version       string            // 8.0 / 8.4 etc.
	Role          string            // "primary" | "replica"
	SemiSync      bool              // SemiSync replication
	GroupRepl     bool              // Group Replication
	Options       map[string]string // user overrides; "server-id" is dropped
	TotalMemoryMB int               // memory budget for "25%"-style defaults
}

// RenderMySQLConf returns the my.cnf body the agent writes into
// /etc/mysql/my.cnf. server-id and report_host are emitted as placeholders;
// the agent substitutes them with per-node values when writing the file.
func RenderMySQLConf(opts RenderMySQLConfOpts) string {
	version := opts.Version
	if version == "" {
		version = "8.0"
	}
	defaults := types.MySQLDefaults(version)

	defaults["server-id"] = MySQLServerIDPlaceholder
	defaults["gtid_mode"] = "ON"
	defaults["enforce_gtid_consistency"] = "ON"
	defaults["log_bin"] = "mysql-bin"

	if opts.SemiSync {
		if opts.Role == "primary" {
			defaults["plugin-load-add"] = "semisync_source.so"
			defaults["rpl_semi_sync_source_enabled"] = "1"
		} else {
			defaults["plugin-load-add"] = "semisync_replica.so"
			defaults["rpl_semi_sync_replica_enabled"] = "1"
		}
	}
	if opts.GroupRepl {
		defaults["binlog_checksum"] = "NONE"
		defaults["plugin-load-add"] = "group_replication.so"
		defaults["report_host"] = MySQLLocalHostPlaceholder
	}

	// User options override defaults — except server-id which the agent
	// always re-applies per node.
	for k, v := range opts.Options {
		if k == "server-id" {
			continue
		}
		defaults[k] = v
	}

	// Resolve "25%"-style placeholders against the topology memory budget,
	// then translate to MySQL's M/G suffixes (Postgres uses MB/GB).
	ResolveMemoryPercents(defaults, opts.TotalMemoryMB)

	keys := make([]string, 0, len(defaults))
	for k := range defaults {
		keys = append(keys, k)
	}
	sort.Strings(keys)

	var b strings.Builder
	b.WriteString("# Generated by stroppy-agent\n[mysqld]\n")
	for _, k := range keys {
		v := defaults[k]
		v = strings.ReplaceAll(v, "MB", "M")
		v = strings.ReplaceAll(v, "GB", "G")
		fmt.Fprintf(&b, "%s = %s\n", k, v)
	}
	return b.String()
}

// SubstituteMySQLPlaceholders fills in per-node values that the renderer left
// as placeholders. The agent calls this on both rendered and override bodies
// just before writing the file.
func SubstituteMySQLPlaceholders(body string, nodeIndex int, localHost string) string {
	body = strings.ReplaceAll(body, MySQLServerIDPlaceholder, fmt.Sprintf("%d", nodeIndex+1))
	body = strings.ReplaceAll(body, MySQLLocalHostPlaceholder, localHost)
	return body
}
